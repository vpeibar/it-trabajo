package model;

import java.util.Date;

/**
 *
 * @author maria
 */
public class Pedido {
    private int id;
    private int cliente_id;
    private Date fecha;
    private String estado;

    public Pedido(int id, int cliente_id, Date fecha, String estado) {
        this.id = id;
        this.cliente_id = cliente_id;
        this.fecha = fecha;
        this.estado = estado;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCliente_id() {
        return cliente_id;
    }

    public void setCliente_id(int cliente_id) {
        this.cliente_id = cliente_id;
    }

    public Date getFecha() {
        return fecha;
    }

    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
}
